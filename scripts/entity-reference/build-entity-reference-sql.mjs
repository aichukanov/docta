#!/usr/bin/env node
/**
 * Build SQL insert file from data/entity-reference/{lab-tests,medical-services}.json
 * into lab_test_reference_info / medical_service_reference_info (migration 009).
 *
 * FK resolved by slug via subquery (JSON has no id, only slug).
 *
 * Usage: node scripts/entity-reference/build-entity-reference-sql.mjs
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..', '..');

const LOCALES = ['en', 'sr', 'sr_cyrl', 'ru', 'de', 'tr'];
// {json field} -> {sql column prefix} — "when" is a reserved word in SQL
const FIELDS = [
	['what', 'what'],
	['how', 'how'],
	['when', 'indications'],
	['prep', 'prep'],
	['abnormal', 'abnormal'],
];

function esc(s) {
	if (s == null) return 'NULL';
	return "'" + String(s).replace(/\\/g, '\\\\').replace(/'/g, "''") + "'";
}

function loadJson(path) {
	return JSON.parse(readFileSync(resolve(ROOT, path), 'utf-8'));
}

function buildInserts(items, table, fkColumn, parentTable) {
	const lines = [];
	const columns = FIELDS.flatMap(([, col]) =>
		LOCALES.map((loc) => `${col}_${loc}`),
	);

	for (const item of items) {
		const values = FIELDS.flatMap(([field]) =>
			LOCALES.map((loc) => esc(item.translations[loc]?.[field])),
		);
		const fkSubquery = `(SELECT id FROM ${parentTable} WHERE slug = ${esc(item.slug)})`;
		const updateClause = columns
			.map((col) => `${col}=VALUES(${col})`)
			.join(', ');

		lines.push(`INSERT INTO ${table} (${fkColumn}, ${columns.join(', ')})`);
		lines.push(`VALUES (${fkSubquery}, ${values.join(', ')})`);
		lines.push(`ON DUPLICATE KEY UPDATE ${updateClause};`);
		lines.push('');
	}

	return lines.join('\n');
}

const labTests = loadJson('data/entity-reference/lab-tests.json');
const medicalServices = loadJson('data/entity-reference/medical-services.json');

const out = [
	'-- Импорт справочного контента (что/как/когда/подготовка/отклонение) —',
	'-- data/entity-reference/{lab-tests,medical-services}.json -> lab_test_reference_info /',
	'-- medical_service_reference_info (схема из migrations/009-entity-reference-info.sql).',
	'--',
	'-- FK резолвится по slug через подзапрос — в JSON нет id, только slug.',
	'-- ON DUPLICATE KEY UPDATE — повторный запуск скрипта безопасен (перезаписывает по FK).',
	'--',
	'-- Generated by scripts/entity-reference/build-entity-reference-sql.mjs — не редактировать руками,',
	'-- при изменении текстов править JSON и пересобирать.',
	'--',
	'-- Apply:',
	'--   mysql -u docta_admin -p --default-character-set=utf8mb4 docta_me < server/sql/migrations/insert-entity-reference-info.sql',
	'',
	'SET NAMES utf8mb4;',
	'',
	buildInserts(
		labTests,
		'lab_test_reference_info',
		'lab_test_id',
		'lab_tests',
	),
	buildInserts(
		medicalServices,
		'medical_service_reference_info',
		'medical_service_id',
		'medical_services',
	),
].join('\n');

const outPath = resolve(
	ROOT,
	'server/sql/migrations/insert-entity-reference-info.sql',
);
writeFileSync(outPath, out, 'utf-8');
console.log(
	`${outPath} (${labTests.length} lab tests + ${medicalServices.length} medical services)`,
);
