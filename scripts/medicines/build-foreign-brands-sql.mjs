// Consolidates data/med-foreign-brands/batch-*.json into an idempotent SQL insert.
// - Matches each substance to med_substances by name_en / name_ru (via translation batches),
//   which gives the med_substances.name natural key used for the SQL subquery join.
// - Substances with no match get a curated INSERT into med_substances (keyed on Latin INN).
// - Emits server/sql/migrations/insert-med-foreign-brands.sql and prints a report.
//
// Run: node scripts/medicines/build-foreign-brands-sql.mjs

import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..', '..');
const BRANDS_DIR = join(ROOT, 'data', 'med-foreign-brands');
const TRANS_DIR = join(ROOT, 'data', 'med-translations', 'substances');
const OUT_SQL = join(ROOT, 'server', 'sql', 'migrations', 'insert-med-foreign-brands.sql');

const norm = (s) => (s || '').toString().trim().toLowerCase().replace(/\s+/g, ' ');
const sqlStr = (s) => (s === null || s === undefined || s === '')
  ? 'NULL'
  : `'${s.toString().replace(/'/g, "''")}'`;

// strip route/form qualifiers so "hydrocortisone (topical)" matches "hydrocortisone"
const cleanInn = (s) => {
  let x = norm(s);
  x = x.replace(/\([^)]*\)/g, ' ');   // drop parentheticals
  x = x.split('/')[0];                // before slash
  x = x.split(',')[0];                // before comma
  x = x.replace(/\b(eye|ophthalmic|otic|ear|nasal|nose|topical|dermal|cream|gel|ointment|oral|throat|spray|drops|lozenge|single|dose|uti|sodium|hydrochloride|hcl)\b/g, ' ');
  return x.replace(/\s+/g, ' ').trim();
};

// ---- load translation substances: build en/ru/de/sr -> src(name) maps ----
const enToSrc = new Map();
const ruToSrc = new Map();
const deToSrc = new Map();
const srToSrc = new Map();
const srcSet = new Set();
const allEn = [];
for (const f of readdirSync(TRANS_DIR).filter((f) => f.endsWith('.json'))) {
  const rows = JSON.parse(readFileSync(join(TRANS_DIR, f), 'utf8'));
  for (const r of rows) {
    if (r.src) srcSet.add(norm(r.src));
    if (r.en) allEn.push({ en: norm(r.en), src: r.src });
    const put = (m, k) => { if (k && r.src && !m.has(norm(k))) m.set(norm(k), r.src); };
    put(enToSrc, r.en); put(enToSrc, cleanInn(r.en));
    put(ruToSrc, r.ru); put(ruToSrc, cleanInn(r.ru));
    put(deToSrc, r.de); put(srToSrc, r.sr);
  }
}

// ---- load brand batches ----
const substances = [];
for (const f of readdirSync(BRANDS_DIR).filter((f) => /^batch-\d+\.json$/.test(f)).sort()) {
  substances.push(...JSON.parse(readFileSync(join(BRANDS_DIR, f), 'utf8')));
}

// ---- match ----
const matched = [];   // { sub, srcName, via }
const missing = [];   // { sub }  -> need curated med_substances row
for (const sub of substances) {
  const en = norm(sub.inn_en), enc = cleanInn(sub.inn_en);
  const ru = norm(sub.inn_ru), ruc = cleanInn(sub.inn_ru);
  let srcName = null, via = null;
  const tryKey = (m, k, label) => { if (!srcName && k && m.has(k)) { srcName = m.get(k); via = label; } };
  tryKey(enToSrc, en, 'en'); tryKey(enToSrc, enc, 'en~');
  tryKey(ruToSrc, ru, 'ru'); tryKey(ruToSrc, ruc, 'ru~');
  tryKey(deToSrc, enc, 'de'); tryKey(srToSrc, enc, 'sr');
  if (!srcName && srcSet.has(enc)) { srcName = sub.inn_en; via = 'src'; }
  if (srcName) matched.push({ sub, srcName, via });
  else {
    // suggest candidates for eyeballing (substring on cleaned INN head)
    const head = enc.slice(0, 5);
    const cand = head.length >= 4 ? allEn.filter((e) => e.en.includes(head)).map((e) => e.en).slice(0, 4) : [];
    missing.push({ sub, cand });
  }
}

// ---- build SQL ----
const lines = [];
lines.push('-- Foreign trade names for tourist-relevant substances.');
lines.push('-- Generated by scripts/medicines/build-foreign-brands-sql.mjs — idempotent.');
lines.push('-- Prereq: 015-med-foreign-brands.sql (table). Apply AFTER medicines are loaded.');
lines.push('-- Run: mysql -u root -p --default-character-set=utf8mb4 docta_me < server/sql/migrations/insert-med-foreign-brands.sql');
lines.push('');
lines.push('SET NAMES utf8mb4 COLLATE utf8mb4_unicode_ci;');
lines.push('');

// Phase 1 imports brands ONLY for substances present in the CInMED register (they have a
// medicine card where the tab can render). Substances absent from the register are deferred
// to Phase 2 (substance pages / reverse search), where "not sold in ME" needs per-item
// truth-check (genuine gap like Enterosgel vs scrape gap like cetirizine). So: NO curated
// med_substances rows here, and NO brand rows for unmatched substances.
lines.push('-- ---------------------------------------------------------------------------');
lines.push('-- Foreign brands for register-present substances (joined by name; empty markets skipped).');
lines.push('-- ---------------------------------------------------------------------------');
let rowCount = 0;
const perMarket = { RU: 0, DE: 0, PL: 0, US: 0 };
const allRows = matched.map((m) => ({ sub: m.sub, srcName: m.srcName }));
for (const { sub, srcName } of allRows) {
  for (const market of ['RU', 'DE', 'PL', 'US']) {
    const brands = (sub.markets && sub.markets[market]) || [];
    for (const b of brands) {
      if (!b.brand) continue;
      rowCount++;
      if (perMarket[market] !== undefined) perMarket[market]++;
      lines.push(
        `INSERT INTO med_foreign_brands (substance_id, market_code, brand_name, strength, pharma_form, note)`
      );
      lines.push(
        `  SELECT s.id, '${market}', ${sqlStr(b.brand)}, ${sqlStr(b.strength)}, ${sqlStr(b.form)}, ${sqlStr(b.note)}`
      );
      lines.push(`  FROM med_substances s WHERE s.name = ${sqlStr(srcName)}`);
      lines.push(
        `  ON DUPLICATE KEY UPDATE strength=VALUES(strength), pharma_form=VALUES(pharma_form), note=VALUES(note);`
      );
    }
  }
}

writeFileSync(OUT_SQL, lines.join('\n') + '\n', 'utf8');

// deferred (register-absent) substances -> keep their collected data for Phase 2
const DEFERRED = join(BRANDS_DIR, '_deferred-phase2.json');
writeFileSync(DEFERRED, JSON.stringify(missing.map((m) => m.sub), null, 2) + '\n', 'utf8');

// ---- report ----
const report = {
  substancesTotal: substances.length,
  matched: matched.length,
  missing: missing.map((m) => `${m.sub.inn_en}${m.cand && m.cand.length ? '  ?candidates: ' + m.cand.join(', ') : '  [no candidates]'}`),
  brandRows: rowCount,
  perMarket,
  matchedViaRu: matched.filter((m) => m.via === 'ru').map((m) => `${m.sub.inn_en} -> ${m.srcName}`),
};
console.log(JSON.stringify(report, null, 2));
